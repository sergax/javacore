package com.sergax.javacore.chapter18.iterator;

import java.util.*;

public class IteratorDemo {
    public static void main(String[] args) {
        // create array list
        ArrayList<String> al = new ArrayList<>();

        //add elements to array list
        al.add("C");
        al.add("A");
        al.add("E");
        al.add("B");
        al.add("D");
        al.add("F");

        // use iterator for print content array list
        System.out.print("Original content array list : ");
        Iterator<String> itr = al.iterator();
        while (itr.hasNext()) {
            String element = itr.next();
            System.out.print(element + " ");
        }
        System.out.println();

        // modify iterable objects
        ListIterator<String> litr = al.listIterator();
        while (litr.hasNext()) {
            String element = litr.next();
            litr.set(element + "+");
        }

        System.out.print("Changed content array list : ");
        itr = al.iterator();
        while (itr.hasNext()) {
            String element = itr.next();
            System.out.print(element + " ");
        }
        System.out.println();

        // and then display the list in reverse order
        System.out.print("Reverse revised list : ");
        while (litr.hasPrevious()) {
            String element = litr.previous();
            System.out.print(element + " ");
        }
        System.out.println();
    }
}
